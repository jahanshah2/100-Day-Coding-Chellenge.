// Initializes a counter
let count = 0;
// This while loop runs until count is 10
while (count < 10) {
    console.log("Hello, World!"); // Logs "Hello, World!"
    count++; // Increments count by 1
}
// Repeats logging "Hello, World!" 10 times, once per loop iteration.
